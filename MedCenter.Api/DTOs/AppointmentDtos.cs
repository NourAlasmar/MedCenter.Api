// هذا الملف يحتوي على الـ DTO الخاص بإنشاء المواعيد (AppointmentCreateDto)
// الغرض منه هو نقل بيانات إنشاء موعد جديد بين واجهة المستخدم (Frontend) وطبقة الـ API (Backend).
// الـ DTO (Data Transfer Object) يُستخدم لتبسيط عملية استقبال البيانات والتحقق منها قبل تخزينها في قاعدة البيانات.
// هذا الكائن يُستخدم في عملية إضافة موعد جديد من قبل الطبيب أو موظف الاستقبال، ويحتوي على جميع المعلومات الأساسية اللازمة لتحديد الموعد.

using MedCenter.Api.Models;

namespace MedCenter.Api.DTOs
{
    public record AppointmentCreateDto(
        long CenterId,           // رقم المركز الطبي الذي ينتمي إليه الموعد
        Guid DoctorId,           // المعرف الفريد للطبيب الذي سيتم تحديد الموعد له
        long? PatientId,         // معرف المريض (اختياري في حال لم يتم تحديد المريض بعد)
        DateTime AppointmentDate, // تاريخ ووقت الموعد
        int DurationMinutes,     // مدة الموعد بالدقائق (مثلاً: 30 دقيقة، 60 دقيقة)
        AppointmentType Type,    // نوع الموعد (معاينة، عملية، استشارة... إلخ)
        string? Notes            // ملاحظات إضافية اختيارية (مثلاً: "مريض جديد" أو "إجراء تجميلي")
    );
}