// هذا الملف يحتوي على كائنات نقل البيانات (DTOs) الخاصة بإنشاء وتحديث بيانات المراكز الطبية
// الغرض منها هو فصل بيانات الإدخال التي تأتي من واجهة المستخدم عن النماذج (Models) الفعلية داخل قاعدة البيانات.
// هذه الـ DTOs تُستخدم في واجهات الـ API الخاصة بالمشرف (Admin) أو الدعم الفني لإضافة أو تعديل بيانات مركز طبي.

namespace MedCenter.Api.DTOs
{
    // DTO لإنشاء مركز طبي جديد
    // يُستخدم عند إضافة مركز جديد للنظام (من قبل الأدمن أو لوحة الإدارة)
    public record CenterCreateDto(
        string Name,          // اسم المركز الطبي (إجباري)
        string? Address,      // عنوان المركز (اختياري)
        string? Phone,        // رقم الهاتف للتواصل (اختياري)
        string? Email         // البريد الإلكتروني الرسمي للمركز (اختياري)
    );

    // DTO لتحديث بيانات مركز طبي موجود
    // يُستخدم عند تعديل بيانات مركز قائم مثل تغيير الاسم أو تعطيله مؤقتًا
    public record CenterUpdateDto(
        string Name,          // اسم المركز الطبي (إجباري)
        string? Address,      // عنوان المركز (اختياري)
        string? Phone,        // رقم الهاتف (اختياري)
        string? Email,        // البريد الإلكتروني (اختياري)
        bool IsActive         // حالة تفعيل المركز (true = نشط، false = معطل)
    );
}