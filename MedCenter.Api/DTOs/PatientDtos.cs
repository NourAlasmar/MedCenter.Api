// هذا الملف يحتوي على كائنات نقل البيانات (DTOs) الخاصة بإدارة بيانات المرضى داخل النظام.
// الغرض منها هو تبسيط عمليات إنشاء وتحديث بيانات المرضى بين الواجهة الأمامية (Frontend)
// وطبقة الـ API دون الحاجة لاستخدام نموذج قاعدة البيانات مباشرة.
// هذه الـ DTOs تُستخدم في واجهات مثل الاستقبال (Reception) أو الطبيب عند تسجيل أو تعديل بيانات مريض.

namespace MedCenter.Api.DTOs
{
    // -----------------------------------------
    // DTO لإنشاء مريض جديد (Patient Creation)
    // -----------------------------------------
    public record PatientCreateDto(
        long CenterId,     // رقم المركز الطبي الذي ينتمي إليه المريض
        string FullName,   // الاسم الكامل للمريض (إجباري)
        string? Phone      // رقم الهاتف للتواصل (اختياري)
    );

    // -----------------------------------------
    // DTO لتحديث بيانات مريض موجود (Patient Update)
    // -----------------------------------------
    public record PatientUpdateDto(
        string FullName,       // الاسم الكامل للمريض (إجباري)
        string? Phone,         // رقم الهاتف (اختياري)
        byte? Gender,          // الجنس: (1 = ذكر، 2 = أنثى) — اختياري لتفادي فرض التحديد عند كل تحديث
        DateTime? BirthDate,   // تاريخ الميلاد (اختياري)
        string? Notes          // ملاحظات إضافية عن المريض (مثل: "يعاني من الحساسية" أو "مراجع دائم")
    );
}